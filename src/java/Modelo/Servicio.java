
package Modelo;


public class Servicio {
    int id_ser;
    String name_ser;

    public Servicio() {
    }

    public Servicio(String name_ser) {
        this.name_ser = name_ser;
    }

    public int getId_ser() {
        return id_ser;
    }

    public void setId_ser(int id_ser) {
        this.id_ser = id_ser;
    }

    public String getName_ser() {
        return name_ser;
    }

    public void setName_ser(String name_ser) {
        this.name_ser = name_ser;
    }
    
    

    
    
}
